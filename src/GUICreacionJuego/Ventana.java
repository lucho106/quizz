    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Hero}
        
        
       if(m==0){
        String HX = JOptionPane.showInputDialog(null, "posicion del heroe en x");
        String HY = JOptionPane.showInputDialog(null, "posicion del heroe en y");
        int hx,hy;
        hx=Integer.parseInt(HX);
        hy=Integer.parseInt(HY);
        
        
        try{
            FileInputStream fstream = new FileInputStream("url");
            DataInputStream entrada = new DataInputStream(fstream);
            BufferedReader buffer = new BufferedReader(new InputStreamReader(entrada));
            String strLinea;
            
            while ((strLinea = buffer.readLine()) != null)   {
            
            File f;
            f = new File(strLinea,"hero");
            try{FileWriter w = new FileWriter(f);
            BufferedWriter bw = new BufferedWriter(w);
            PrintWriter wr = new PrintWriter(bw);  
            wr.write(hx+" "+hy);
            wr.close();
            bw.close();
            }catch(IOException e){};
           
            File fhero = new File("urlhero");
try{
FileWriter w = new FileWriter(fhero);
BufferedWriter bw = new BufferedWriter(w);
PrintWriter wr = new PrintWriter(bw);  
wr.write(f.getAbsolutePath());

wr.close();
bw.close();
}catch(IOException e){};
            }
            entrada.close();
        }catch (Exception e){ 
            System.err.println("Ocurrio un error: " + e.getMessage());
        }
        
        }else{JOptionPane.showMessageDialog(null,"NO SE PUEDE TENER MAS DE UN HEROE"); }
       
       m++;
        
    }//GEN-LAST:event_jButton1ActionPerformed

