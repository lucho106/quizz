    private void ObjetivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ObjetivoActionPerformed
     //+Objetivo
        String OBJX = JOptionPane.showInputDialog(null, "posicion del objetivo "+n+" en x");
        String OBJY = JOptionPane.showInputDialog(null, "posicion del objetivo "+n+" en y");
        
        Objetivosx.add(Integer.parseInt(OBJX));
        Objetivosy.add(Integer.parseInt(OBJY));
        for (int i = 0; i < n ; i++) {
            System.out.print(Objetivosx.get(i) + " ");
            System.out.print(Objetivosy.get(i));
        }
        
        n++;
        
    }//GEN-LAST:event_ObjetivoActionPerformed

